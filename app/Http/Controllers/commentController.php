     * @return \Illuminate\Http\Response
     */
    public function send(Request $request)
    {
        $comments = [
            "name" => $request->name,
            "email" => $request->email,
            "comment" => $request->comment,
            "service_id" => $request->service_id,
        ];
        comment::create($comments);
        return back()->with("success", "Commented Successfully");
    }

